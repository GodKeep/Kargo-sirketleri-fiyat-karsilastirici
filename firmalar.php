2003 yılında kurulan MNG Kargo 850’den fazla şubesi, 9 tanesi Teknolojik Aktarma Merkezi olmak üzere toplam 27 aktarma merkezi, 35 minihub, 14 bölge müdürlüğü, 11.000’in üzerinde çalışanı, yaklaşık 3500 kara taşıma aracı ile hizmet vermektedir. MNG Kargo, Türkiye’nin bir ucundan diğer ucuna ve dünyada 220 farklı ülkede, günde 700 bin adrese dünya standartlarında hizmet ulaştıran Türkiye’nin lider kargo şirketlerindendir. Sektörde ilk Teknolojik Aktarma Merkezlerine sahip kargo şirketi olan MNG Kargo, bu teknoloji sayesinde saatte 65 bin kargoyu yüksek hızda ayrıştırıp, el değmeden hatasız olarak hat araçlarına yükleyerek, hasarsız teslimat sağlamaktadır. 2000 yılından bu yana Türkiye’ye 5 milyar dolardan fazla yatırım getirmeyi başaran Turkven’in danışmanlığını yaptığı kurumsal yatırımcılar ve başta Haydar Sancak ile Suat Sancak olmak üzere, Sancak Ailesi 2017 yılında MNG Kargo’yu satın almıştır. Türkiye’de kargo taşımacılığı sektörünün gelişiminde başrol oynayan, dünya standartlarındaki hizmet anlayışı ile ülkemiz taşımacılık sektörüne çağdaş dinamikler kazandırmış olan MNG Kargo güçlenen sermaye yapısıyla teknolojik yatırımlarına devam etmektedir.</p>
