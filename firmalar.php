İlk posta teşkilatı, Tanzimat Fermanı ile yaşanan gelişmelerin sonucu olarak Osmanlı Devleti'nin tüm halkının ve yabancıların posta ihtiyaçlarına cevap vermek amacıyla Nezaret olarak 23 Ekim 1840 tarihinde kurulmuştur. İlk postahane ise İstanbul'da Yeni Camii avlusunda Postahane-i Amire adı ile açılmıştır. İlk memurlar Süleyman Ağa, tahsildar Sofyalı Ağyazar Türkçe dışında yazılmış gönderilerin adreslerini tercüme etmek üzere mütercim olarak atanmışlardır. 1871 yılında ise Posta Nazırlığı ile Telgraf Müdürlüğü birleştirilerek Posta ve Telgraf Nezaretine dönüştürülmüştür. 1876 yılında milletlerarası posta nakli şebekesi kurulmuş, 1901 yılında ise koli ve havale işleminin kabulüne başlanmıştır. 23 Mayıs 1909 tarihinde ilk manuel telefon santralının İstanbul'da hizmete verilmesinden sonra Posta ve Telgraf Nezareti, 1954 yılında Kamu İktisadi Teşebbüsü (KİT) olan PTT Genel Müdürlüğü, 1984 yılında Kamu İktisadi Devlet Teşebbüslerinin yeniden düzenlenmesi ile ilgili olarak çıkarılan 233 sayılı KHK ile Kamu İktisadi Kuruluşu (KİK) statüsüne geçirilmiştir. 18 Haziran 1994 tarih ve 4000 sayılı Kanun ile PTT İşletmesi Genel Müdürlüğünün, T.C. Posta İşletmesi Genel Müdürlüğü ve Türk Telekomünikasyon Anonim Şirketi şeklinde yeniden yapılanması öngörülmüş olup, 24 Nisan 1995 tarihinden itibaren T.C. Posta İşletmesi Genel Müdürlüğü müstakilen çalışmaya başlamıştır. 29 Ocak 2000 tarih ve 23948 sayılı Resmi Gazetede yayımlanarak yürürlüğe giren 4502 sayılı kanunun 24. maddesi ile "T.C. Posta İşletmesi Genel Müdürlüğü" olan Kuruluşun adı "T.C. Posta ve Telgraf Teşkilatı Genel Müdürlüğü" (PTT) olarak değiştirilmiştir.</p>
