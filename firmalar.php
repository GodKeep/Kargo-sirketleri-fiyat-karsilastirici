1982 yılında İstanbul'da kurulmuş kargo şirketi.İbrahim Arıkan tarafından Arıkanlı Holding çatısı altında kuruldu. Türkiye’deki ilk Türk kargo markasıdır. Başlangıçta Türkiye'ye, sonrasında KKTC'ye gönderiler yapan firma 1996 yılından itibaren yurt dışına gönderiler yapmaktadır. 2020 yılı itibarıyla Türkiye'nin 81 ilinde ve Kuzey Kıbrıs Türk Cumhuriyeti'nde hizmet noktaları vardır. 17 Bölge Müdürlüğü, 33 Aktarma Merkezi, 1200’den fazla şubesi, 20.000’den fazla çalışanı ve 6000'in üzerinde araç filosu mevcuttur. Fransız posta şirketi La Poste şemsiyesi altında faaliyet gösteren çözüm ortağı Geopost ile birlikte 2003 yılından bu yana dünyada 220’den fazla noktaya uluslararası gönderi taşımaktadır. Firmanın "'VIP, Gün Tercihli, Tahsilatlı Gönderi (Kapıda ödeme sistemi)" gibi hizmetleri vardır. Firma, 2006 yılında kargo paketlemesi hizmetlerine başlamıştır. Normal kargo poşeti, standart kutular, hediye kutuları, rulo tip paketler ve Türkiye'deki kargoculukta ilk olarak, kırılabilecek, hassas, elektronik eşyalar için Havalı Paket gibi paketleme alternatifleri vardır. 2011 yılından itibaren de İnternet Şube, iPad ve iPhone üzerinden online kargo işlem hizmetleri başlatmışlardır.</p>
