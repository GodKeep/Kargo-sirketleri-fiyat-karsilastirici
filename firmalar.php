1907 yılında 19 yaşında bir girişimci olan Jim Casey, Amerika’nın Seattle şehrinde arkadaşından 100 dolar borç alarak American Messenger Company'i (Amerikan Kurye Şirketi) kurdu. Teslimatları yaya olarak veya bisikletle yapan Casey, 1919 yılında şirketi genişleterek Seattle sınırlarının dışına çıkmış ve şirkete şu anki ismi olan United Parcel Service (Birleşik Paket Hizmeti) adını vermiştir. Zaman içinde Amerika’daki diğer eyaletlerde ve ardından diğer kıtalarda da hizmet vermeye başlayan UPS, dünyanın en büyük paket teslimat şirketi olmayı başarmış, özel taşıma ve lojistik hizmetlerinde küresel bir lider olmuştur. Avrupa pazarına ilk olarak 1976 yılında giren UPS, o günden bu yana uluslararası gücü ve yüksek kalite standartları ile yerel uzmanlığını her pazarda birleştirerek kapsamlı Avrupa hizmet portföyünü geliştirmiştir. Yetkili Servis Sağlayıcısı Ünsped ile1988 yılında Türkiye pazarına giren UPS, 2009 yılında Ünsped’i satın almıştır. UPS Türkiye, 3.000’e yakın çalışanı, 260’tan fazla şube ve yetkili servis sağlayıcısı ve 700 araçlık filosuyla Türkiye pazarında da sektörün lider firmaları arasında yerini almıştır.</p>
